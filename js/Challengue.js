//CHALLEGUE

//Usando for

for(var i=0; i<10; i++){
    console.log("Ciclo FOR, valor de la variable i actual:", i);
}
console.log(i);

//Usando while{
let counter = 0;

while(counter < 10){
    console.log("Ciclo WHILE, valor actual del counter:", counter);
    counter++;
}
console.log(counter);

//BONUS XTRA
counter = 0;
do{
    console.log("Ciclo Do while, valor actual de la variable counter:", counter);
    counter ++;
} while(counter<10);

console.log(counter);

